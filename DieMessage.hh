_Pragma ( "once" );

#include <iostream>
#include <string>
#include <cstdlib>

ssize_t static const _BufferSize = 100;

inline void _DieWithUserMessage( std::string const& _Msg
                               , std::string const& _Detail
                               )
    {
    std::cerr << _Msg << ": "
              << _Detail
              << std::endl;

    std::quick_exit( EXIT_FAILURE );
    }

inline void _DieWithSystemMessage( std::string const& _Msg )
    {
    std::perror( _Msg.c_str() );

    std::quick_exit( EXIT_FAILURE );
    }

